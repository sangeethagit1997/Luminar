package controlstatements;

public class IterativeStatements {

	public static void main(String[] args) {
		// for loop - print 1 to 10
		for(int i=1;i<=10;i++) {
		System.out.println(i);	
		}
		
		//for loop - print name
		String name="Sangeetha";
		for(int j=1;j<=10;j++) {
			System.out.println(name);
		}
		
		//for loop - multiplication table of 5
		int n=5;
		int k=1;
	
		for(k=1;k<=10;k++) {
			int m=n*k;
			System.out.println(k+"*"+n+"="+m);
		}
		

	}

}
