package basicjava;

public class DataTypes {

	public static void main(String[] args) {
		byte b=5;
		System.out.println("The value of byte ="+ b);
		 
		short s=50;
		System.out.println("The value of short ="+ s);
		
		int a=5;
		System.out.println("The value of integer ="+ a);
		
		long lg=557778265553l;
		System.out.println("The value of long ="+ lg);
		 
		float f=7.8f;
		System.out.println("The value of float ="+ f);
		
		double d=5.455525244;
		System.out.println("The value of double ="+ d);
		 
		char c='v';
		System.out.println("The value of byte ="+ c);
		 
		char asciiChar=65;
		System.out.println("The value of asciiChar ="+ asciiChar);
		 
		boolean B=20>40;
		System.out.println("The value of boolean ="+B);
		
		String st="Sangeetha";
		System.out.println("String value is" + st);
				
		
	}

}
